<template>
  <div class="tab-control">
    <div v-for="(item, index) in titles" 
    :class= "{active: index === currentIndex}" class="tab-control-item" @click="itemClick(index)"
    :key="index">
      <span>{{item}}</span>
    </div>
  </div>
</template>

<script>
  export default {
    name: 'TabControl',
    props: {
      titles: {
        type: Array,
        default() {
          return []
        }
      }
    },
    data() {
      return {
        currentIndex: 0
      }
    },
    methods: {
      itemClick(index) {
        this.currentIndex = index
        this.$emit('tabClick', index)
      }
    }
  }
</script>

<style scoped>
  .tab-control {
    display: flex;
    text-align: center;
    font-size: 13px;
    height: 35px;
    line-height: 30px;
  }
  .tab-control span {
    padding: 5px;
  }
  .tab-control-item {
    flex: 1;
  }
  .active {
    color: var(--color-tint);
  }
  .active span {
    border-bottom: 2px solid var(--color-tint);
  }
</style>